import com.ruijc.http.HttpClient;
import org.jsoup.Jsoup;
import org.jsoup.nodes.Document;
import org.jsoup.select.Elements;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.stereotype.Service;

import java.util.ArrayList;
import java.util.List;

/**
 * 凤凰网接口
 *
 * @author Storezhang
 * @create 2017-04-13 16:03
 * @email storezhang@gmail.com
 * @qq 160290688
 */
@Service
public class IfengApi {

    @Autowired
    private HttpClient client;

    public List<String> getNews() {
        List<String> news = new ArrayList<String>(300);

        String ifeng = client.get("http://www.ifeng.com/");
        Document doc = Jsoup.parse(ifeng);
        Elements element = doc.select(".FNewMTopLis").select("ul li a");
        for (int i = 0; i < element.size(); ++i) {
            news.add(element.get(i).text());
        }

        return news;
    }
}
